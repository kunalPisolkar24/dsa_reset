#include <iostream>
#include <vector>
#include "Solution.cpp"

using namespace std;

struct TestCase {
    int n;
    vector<vector<int>> edges;
    int expected;
};

int main() {
    Solution sol;
    vector<TestCase> cases = {
    { 9, {{0, 4}, {3, 4}, {1, 5}, {6, 8}, {1, 7}, {0, 2}, {7, 8}, {3, 6}, {0, 8}, {3, 5}, {2, 8}}, 4 },
    { 2, {}, 2 },
    { 7, {{0, 3}, {0, 4}, {1, 5}}, 4 },
    { 4, {}, 4 },
    { 20, {{12, 19}, {14, 16}, {3, 13}, {8, 9}, {4, 18}, {2, 11}, {13, 14}, {6, 17}, {4, 8}, {3, 18}, {12, 18}, {10, 14}, {1, 14}, {15, 16}, {4, 16}, {14, 17}, {5, 17}, {10, 13}, {0, 6}, {11, 12}, {2, 3}, {10, 16}, {0, 12}, {11, 15}, {0, 15}, {7, 14}}, 11 },
    { 14, {{3, 8}, {5, 12}, {6, 11}, {4, 9}, {3, 7}, {0, 3}, {4, 12}, {0, 9}, {12, 13}, {5, 10}, {5, 6}, {5, 9}, {0, 8}, {1, 3}, {1, 9}, {3, 5}}, 9 },
    { 2, {}, 2 },
    { 5, {{1, 3}, {0, 3}, {0, 4}, {3, 4}}, 4 },
    { 5, {{1, 3}}, 4 },
    { 11, {{0, 7}, {3, 8}, {2, 4}, {0, 4}, {2, 10}, {0, 9}, {8, 10}, {6, 7}, {5, 6}, {6, 10}, {1, 9}}, 5 },
    { 6, {{2, 4}}, 5 },
    { 17, {{11, 13}, {8, 10}, {10, 13}, {0, 6}, {7, 12}, {5, 6}, {5, 12}}, 13 },
    { 13, {{0, 1}, {3, 8}, {10, 11}, {3, 4}, {1, 8}, {4, 12}, {2, 9}}, 8 },
    { 10, {{3, 8}, {0, 3}, {4, 5}, {4, 8}, {3, 6}, {7, 8}}, 7 },
    { 12, {{9, 10}, {1, 3}, {0, 1}, {1, 2}, {6, 9}, {4, 9}, {5, 7}, {0, 11}, {7, 9}, {6, 7}, {8, 9}, {2, 11}, {3, 5}, {8, 11}}, 7 }
};

    
    int passed = 0;
    for (int i = 0; i < cases.size(); i++) {
        TestCase tc = cases[i];
        int result = sol.countEvenDepthNodes(tc.n, tc.edges);
        if (result == tc.expected) {
            cout << "Test " << i + 1 << ": PASSED\n";
            passed++;
        } else {
            cout << "Test " << i + 1 << ": FAILED\n";
            cout << "Expected: " << tc.expected << " | Got: " << result << "\n";
        }
    }
    
    cout << "\nResult: " << passed << " / " << cases.size() << " tests passed.\n";
    return 0;
}
