#include <iostream>
#include <vector>
#include "Solution.cpp"

using namespace std;

struct TestCase {
    int n;
    vector<vector<int>> edges;
    int expected;
};

int main() {
    Solution sol;
    vector<TestCase> cases = {
    { 21, {}, 0 },
    { 10, {{0, 7}, {1, 4}, {0, 6}, {6, 7}, {0, 5}, {3, 6}, {0, 8}, {7, 8}}, 6 },
    { 25, {{20, 23}, {13, 18}, {3, 10}, {12, 15}, {0, 14}, {1, 9}, {7, 14}}, 7 },
    { 3, {{0, 1}}, 1 },
    { 6, {{2, 5}, {3, 4}}, 2 },
    { 14, {{3, 13}}, 1 },
    { 20, {{2, 17}, {16, 19}, {9, 12}, {3, 10}, {18, 19}, {10, 13}, {5, 13}, {0, 5}, {5, 19}}, 9 },
    { 7, {}, 0 },
    { 10, {{0, 1}, {3, 7}, {0, 3}, {2, 9}, {1, 3}}, 4 },
    { 19, {{3, 14}, {0, 10}, {1, 15}, {7, 10}, {11, 13}, {15, 17}, {1, 14}, {3, 7}, {11, 12}, {2, 3}, {0, 2}, {1, 13}, {3, 9}, {4, 8}, {2, 18}, {11, 18}, {5, 18}}, 15 },
    { 23, {{0, 4}, {17, 19}, {5, 7}, {3, 16}, {1, 10}}, 5 },
    { 22, {{15, 21}, {5, 8}, {4, 9}, {11, 13}, {0, 8}, {19, 21}}, 6 },
    { 15, {{2, 14}, {3, 4}, {4, 13}, {1, 5}, {10, 14}, {6, 8}, {8, 10}, {8, 13}, {7, 12}, {4, 14}}, 9 },
    { 24, {{12, 19}, {8, 18}, {9, 20}, {7, 10}, {6, 14}, {4, 14}, {5, 9}, {8, 11}, {0, 7}, {0, 4}, {10, 14}, {2, 10}, {15, 16}, {6, 13}, {4, 13}, {12, 20}, {4, 19}, {14, 20}, {5, 17}, {7, 8}}, 17 },
    { 3, {{0, 2}}, 1 }
};

    
    int passed = 0;
    for (int i = 0; i < cases.size(); i++) {
        TestCase tc = cases[i];
        int result = sol.countTreeEdges(tc.n, tc.edges);
        if (result == tc.expected) {
            cout << "Test " << i + 1 << ": PASSED\n";
            passed++;
        } else {
            cout << "Test " << i + 1 << ": FAILED\n";
            cout << "Expected: " << tc.expected << " | Got: " << result << "\n";
        }
    }
    
    cout << "\nResult: " << passed << " / " << cases.size() << " tests passed.\n";
    return 0;
}
