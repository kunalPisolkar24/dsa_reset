#include <iostream>
#include <vector>
#include "Solution.cpp"

using namespace std;

struct TestCase {
    int n;
    vector<vector<int>> edges;
    int expected;
};

int main() {
    Solution sol;
    vector<TestCase> cases = {
    { 18, {{10, 15}, {5, 15}, {1, 7}, {10, 12}}, 4 },
    { 3, {{0, 2}}, 2 },
    { 7, {{3, 4}}, 2 },
    { 16, {{4, 15}, {9, 14}, {0, 11}}, 2 },
    { 8, {{0, 7}, {3, 7}, {0, 6}, {2, 3}, {4, 5}, {5, 6}, {2, 5}, {4, 7}, {3, 5}}, 7 },
    { 23, {{4, 13}, {0, 10}, {12, 13}, {17, 19}, {10, 16}, {17, 21}, {14, 18}, {14, 15}, {6, 22}}, 3 },
    { 24, {{16, 20}, {7, 17}, {3, 10}, {14, 16}, {5, 16}, {9, 11}, {0, 8}, {8, 21}, {2, 14}, {0, 23}, {0, 20}, {6, 11}, {6, 8}, {7, 16}, {6, 14}, {4, 14}, {12, 18}, {9, 16}, {8, 14}, {0, 22}, {16, 18}, {18, 21}, {7, 21}, {7, 18}, {4, 16}, {12, 23}, {14, 17}, {3, 23}, {10, 13}, {2, 9}, {13, 18}, {6, 9}}, 21 },
    { 23, {{10, 15}, {3, 8}, {12, 16}, {2, 16}, {6, 20}, {14, 16}, {7, 19}, {0, 12}, {0, 22}, {5, 10}, {5, 13}, {8, 12}, {0, 21}, {4, 8}, {1, 16}, {19, 22}, {2, 21}}, 13 },
    { 4, {{0, 2}}, 2 },
    { 18, {{0, 7}, {8, 14}, {3, 8}, {4, 13}, {0, 9}, {5, 13}, {2, 3}, {3, 9}, {2, 6}, {7, 15}, {2, 15}, {4, 17}, {4, 7}, {0, 11}}, 14 },
    { 9, {{2, 4}, {2, 7}, {4, 6}, {6, 7}, {1, 7}, {0, 8}}, 5 },
    { 13, {{1, 4}, {8, 10}, {4, 8}}, 4 },
    { 3, {{0, 1}}, 2 },
    { 19, {{0, 16}, {5, 14}, {0, 9}, {8, 10}, {3, 9}, {2, 18}, {12, 15}, {7, 15}, {1, 9}}, 5 },
    { 21, {{1, 2}, {7, 11}}, 2 }
};

    
    int passed = 0;
    for (int i = 0; i < cases.size(); i++) {
        TestCase tc = cases[i];
        int result = sol.largestComponent(tc.n, tc.edges);
        if (result == tc.expected) {
            cout << "Test " << i + 1 << ": PASSED\n";
            passed++;
        } else {
            cout << "Test " << i + 1 << ": FAILED\n";
            cout << "Expected: " << tc.expected << " | Got: " << result << "\n";
        }
    }
    
    cout << "\nResult: " << passed << " / " << cases.size() << " tests passed.\n";
    return 0;
}
